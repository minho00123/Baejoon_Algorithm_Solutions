def solution(my_string):
    answer = ''
    for i in range(len(my_string)):
        if my_string[i] == 'a':
            answer += ''
        elif my_string[i] == 'e':
            answer += ''
        elif my_string[i] == 'i':
            answer += ''
        elif my_string[i] == 'o':
            answer += ''
        elif my_string[i] == 'u':
            answer += ''
        else:
            answer += my_string[i]
    return answer